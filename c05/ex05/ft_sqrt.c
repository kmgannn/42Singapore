int	ft_sqrt(int nb)
{
	int	odd;
	int	step;

	if (nb <= 0)
		return (0);
	odd = 1;
	step = 0;
	while (nb > 0)
	{
		nb -= odd;
		odd += 2;
		step++;
	}
	if (nb == 0)
		return (step);
	return (0);
}
